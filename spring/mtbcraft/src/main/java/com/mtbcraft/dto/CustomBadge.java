package com.mtbcraft.dto;

public class CustomBadge {
private int cbg_num;
	
	private String cbg_name;
	
	private String cbg_image;
	
	private String cbg_id;

	public String getCbg_id() {
		return cbg_id;
	}

	public void setCbg_id(String cbg_id) {
		this.cbg_id = cbg_id;
	}

	public int getCbg_num() {
		return cbg_num;
	}

	public void setCbg_num(int cbg_num) {
		this.cbg_num = cbg_num;
	}

	public String getCbg_name() {
		return cbg_name;
	}

	public void setCbg_name(String cbg_name) {
		this.cbg_name = cbg_name;
	}

	public String getCbg_image() {
		return cbg_image;
	}

	public void setCbg_image(String cbg_image) {
		this.cbg_image = cbg_image;
	}
}
